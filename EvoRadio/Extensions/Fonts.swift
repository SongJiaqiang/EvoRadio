import UIKit


//MARK: special font size
extension UIFont {
    class func size10() -> UIFont {
        return UIFont.systemFont(ofSize: 10)
    }
    
    class func size12() -> UIFont {
        return UIFont.systemFont(ofSize: 12)
    }
    
    class func size14() -> UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    
    class func size16() -> UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    class func boldSize16() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    
    class func size18() -> UIFont {
        return UIFont.systemFont(ofSize: 18)
    }
    class func boldSize18() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 18)
    }
    
    
    class func size22() -> UIFont {
        return UIFont.systemFont(ofSize: 22)
    }
    class func boldSize22() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 22)
    }

}
